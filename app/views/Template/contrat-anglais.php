<h1>Bike Loan Contract
Université Laval</h1>

<p>Become a member of the bike loan service offered by Université Laval in collaboration with Coop Roue-Libre! 
	Joining our service is free for all employees, students and residents of Université Laval. 
	By becoming a member you are agreeing to respect the following rules:
</p>

<ol>
<li>
When returning your bike, it is to be returned to the same place that you removed it from. 
The space number should correspond to the number on the bike.
</li>
<li>
You have to return the bike and key on the date that was agreed upon when you took possession of the bike. 
Daily bike loans are for a 4 hours period. Overnight bike loans are to be returned by 11h00 the next morning. 
In the case of a holiday, the bikes are to be returned the following day.  
</li>
<li>
The bike is to be returned in the same condition that it was when loaned. 
In the case of a damaged bike you can be held responsible for as much as $250 in the 30 days that follow. 
If there is a problem with your bike please talk to one of the COOP service members. 
</li>
<li>
Université Laval and Coop Roue-Libre assume no responsibility for any damage or injury that may result from bike use. 
</li>
<li>
For each bike loan the user must leave an official piece of identity as a deposit.  Student cards are not accepted.
</li>
<li>
As user, you are responsible for the bike. 
In the case of a lost key, 10$ will be charged. In the case of a lost lock, 40$ will be charged. 
In the case of a stolen bike, the user must replace the total cost of the bike ($450). 
Those fees are to be paid within the 30 days that follow. For this reason, it is crucial to always properly secure the bike with the provided lock. 
</li>
<li>
For each hour that a bike is returned late, the user will be unable to use the bike loan service for 24 hours. 
</li>
<li>
Users that accumulate excessive lateness or those that do not respect the rules will be banned from using the bike loan service.  
</li>
</ol>

<p>
The Coop Roue-Libre invites you to care for the bikes as if they were your own. 
This is to be done out of respect for the other users of this free service provided by Université Laval.
</p>

