<h1>Adhésion au service de prêt de vélo
Université Laval</h1>


<p>
Devenez membre du service de prêt de vélo offert par l’Université Laval en collaboration avec la Coop Roue-Libre! 
L’adhésion au service est gratuite pour tous les employés, les étudiants et les résidents de l’Université Laval. 
En devenant membre, vous vous engagez à respecter les règles suivantes :
</p>

<ol>
<li>
Vous devez remettre le vélo à l’endroit où vous en avez pris possession, soit à l’espace numéroté qui correspond au numéro inscrit sur votre vélo.
</li>
<li>
Vous devez rapporter le vélo et sa clé avant l’échéance de votre prêt. 
Les prêts de jour sont d’une durée de 4 heures alors que les prêts de soir sont valides jusqu’à 11h00 le lendemain matin. 
Lors des congés fériés, les vélos doivent être retournés le matin suivant le congé.
</li>
<li>
Vous devez remettre le vélo dans l’état où il était lors de son emprunt. 
Advenant une utilisation anormale et/ou irresponsable du vélo, vous devrez verser une somme pouvant atteindre 250 $ dans un délai de 30 jours. 
Si vous avez un problème avec un vélo, vous devez en avertir le responsable.
</li>
<li>
L’Université Laval et la Coop Roue-Libre n’assument aucune responsabilité pour tout dommage ou préjudice pouvant être subi par l’utilisateur ou que l’utilisateur pourrait causer à un tiers.
</li>
<li>
Pour chaque prêt de vélo, une carte d’identité officielle avec photo et valide au moment du prêt est obligatoire à titre de dépôt. 
Les cartes étudiants ne sont pas acceptées.
</li>
<li>
Comme utilisateur, vous êtes responsable du vélo. En cas de perte de la clé du cadenas, des frais de 10$ sont exigés. 
Dans le cas de perte du cadenas, des frais de 40$ sont exigés. 
Dans le cas du vol du vélo, l’utilisateur doit rembourser la totalité du coût du vélo, soit 450 $. Ces frais sont payables dans un délai de 30 jours. 
C’est pourquoi il est très important de bien verrouiller en tout temps le vélo à l’aide du cadenas fourni.
</li>
<li>
Pour chaque heure de retard, l'usager sera privé du service de prêt pour 24 heures.
</li>
<li>
Les usagers accumulant trop de retards ou ne respectant pas les règles ci-dessus se verront exclus du service.  
</li>
</ol>

<p>
La Coop Roue-Libre vous invite à prendre soin des vélos comme s’ils étaient les vôtres, par respect pour les autres utilisateurs de ce service offert gratuitement par l’Université Laval.
</p>

